class MFBlock(nn.Module):
    """
    MambaFusion Block - C2fSTBlockçš„å®žçŽ°
    èžåˆCNNã€Swin Transformerå’ŒMGDFISä¸‰ç»„ä»¶
    ä¸“ä¸ºå°ç›®æ ‡æ£€æµ‹ä¼˜åŒ–çš„å¤åˆæ¨¡å—
    
    Args:
        ch_in (int): è¾“å…¥é€šé“æ•°
        ch_hidden (int): éšè—å±‚é€šé“æ•°
    """
    
    def __init__(self, ch_in, ch_hidden):
        super().__init__()
        
        # é€šé“è°ƒæ•´
        self.conv1 = Conv(ch_in, ch_hidden, 1)
        
        # è½»é‡åŒ–Swin Transformerï¼ˆç®€åŒ–ç‰ˆæœ¬ï¼‰
        self.swin_conv = nn.Sequential(
            nn.Conv2d(ch_hidden, ch_hidden, 7, 1, 3, groups=ch_hidden),  # æ·±åº¦å·ç§¯æ¨¡æ‹Ÿçª—å£æ³¨æ„åŠ›
            nn.BatchNorm2d(ch_hidden),
            nn.SiLU(),
            nn.Conv2d(ch_hidden, ch_hidden, 1),  # ç‚¹å·ç§¯è¿›è¡Œé€šé“æ··åˆ
            nn.BatchNorm2d(ch_hidden)
        )
        
        # MGDFISä¸‰å­æ¨¡å—
        self.fusion_lock = FusionLockTSS(ch_hidden)
        self.global_detail = GlobalDetail(ch_hidden) 
        self.dynamic_pixel = DynamicPixelAttn(ch_hidden)
        
        # è¾“å‡ºé€šé“è°ƒæ•´
        self.conv2 = Conv(ch_hidden, ch_in, 1)
        
        # SimAMæ— å‚æ³¨æ„åŠ›
        self.simam = SimAM()
    
    def forward(self, x):
        """å‰å‘ä¼ æ’­ï¼šä¾æ¬¡é€šè¿‡å„å­æ¨¡å—"""
        identity = x
        
        # é€šé“å‡ç»´
        y = self.conv1(x)
        
        # Swin-likeç‰¹å¾å¤„ç†
