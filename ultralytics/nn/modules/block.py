        y = self.dynamic_pixel(y)    # åŠ¨æ€åƒç´ æ³¨æ„åŠ›
        
        # é€šé“é™ç»´
        y = self.conv2(y)
        
        # æ®‹å·®è¿žæŽ¥ + SimAMæ³¨æ„åŠ›
        return self.simam(identity + y)
